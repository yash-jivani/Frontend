/*

Equality Operator

==  : compare without data type 
=== : compare with data type (strict comparison)
!=  : compare without data type 
!== : compare with data type (strict comparison)

*/

console.log(5==5);
console.log(5=='5');
console.log(5==='5');
console.log(5===5);

console.log(5!=5);
console.log(5!==5);
console.log(5!=='5');
console.log(5!='5');